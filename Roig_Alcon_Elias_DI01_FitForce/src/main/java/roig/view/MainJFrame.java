    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        DataAccess da = new DataAccess();
        Usuari usuari = da.getUsuarisEmail(txtEmailLogin.getText());
        if (usuari != null) {
            // comprobar password
            char[] passToVerify = txtPasswordLogin.getPassword();
            String userPasswordHasInDataBase = usuari.getPasswordHash();
            var result = BCrypt.verifyer().verify(passToVerify, userPasswordHasInDataBase);
            
            if (result.verified) {
                JOptionPane.showMessageDialog(this, "Login succesfull: Welcome " + usuari.getNom());
            } else {
                JOptionPane.showMessageDialog(this, "Error: invalid login");
            } 
        } else {
                JOptionPane.showMessageDialog(this, "Error: user not found");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

