    private void btnReadUsuarisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadUsuarisActionPerformed
        // TODO add your handling code here:
        DataAccess da = new DataAccess();
        ArrayList<Usuari> usuaris = da.getUsuaris();

        txtaShowInfoUsuaris.removeAll();

        for (Usuari u : usuaris) {
            txtaShowInfoUsuaris.append(u.getId() + " " + u.getNom() + " " + u.getEmail() + " " + u.getPasswordHash().substring(0, 5) + " " + u.isInstructor() + "\n");
        }
    }//GEN-LAST:event_btnReadUsuarisActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        Usuari nouUsuari = new Usuari();
        nouUsuari.setNom(txtNom.getText());
        nouUsuari.setEmail(txtEmail.getText());
        // bcrypto library
        String passwordHash = BCrypt.withDefaults().hashToString(12, txtPassword.getPassword());
        nouUsuari.setPasswordHash(passwordHash);  // luego lo arregla
        nouUsuari.setInstructor(chkInstructor.isSelected());

        DataAccess da = new DataAccess();
        int nouUsuariId = da.registerUser(nouUsuari);
        nouUsuariId = da.getLastInsertUserId();
        nouUsuari.setId(nouUsuariId);
        lblInfoMessage.setText("User registered succesfully with Id " + nouUsuariId);
    }//GEN-LAST:event_btnRegisterActionPerformed

